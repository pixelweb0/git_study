// 불 대수

/*
불 대수 : 일상적인 논리를 수학적으로 표현한 것.
불 대수의 값, 진리값 : 참(true), 거짓(false)
불 대수의 연산 : and, or, not

명제 : 참, 거짓을 판별할 수 있는 문장.
예) 
대한민국의 수도는 서울이다. (참)
2는 1보다 작다. (거짓)
한국의 수도는 어디입니까? (명제가 아님)
짜장면보다 짬뽕이 맛있다. (명제가 아님)

AND 연산 : x와 y가 모두 참일 때만 x AND y가 참
x     | y     | x AND y
true  | true  | true
true  | false | false
false | true  | false
false | false | false

OR 연산 : x와 y 중 하나라도 참일 때만 x OR y가 참
x     | y     | x OR y
true  | true  | true
true  | false | true
false | true  | true
false | false | false

NOT 연산 : x가 참이면 NOT x는 거짓, x가 거짓이면 NOT x는 참
x     | NOT x
true  | false
false | true


*/
