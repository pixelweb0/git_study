// 블린(Boolean)
// Boolean : 참(true), 거짓(false) 논리값을 반환하는 함수

// 비교 연산자
console.log('비교 연산자');
console.log(2 > 1);
console.log(2 < 1);
console.log(3 >= 2);
console.log(3 <= 3);
console.log(3 === 3);
console.log(3 !== 3);

// 문자열 비교 연산자
console.log('문자열 비교 연산자');
console.log('Hello' === 'Hello');
console.log('Hello' !== 'hello');

// 불 대수 연산자 AND (&&)
console.log('불 대수 연산자 AND (&&)');
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

// 불 대수 연산자 OR (||)
console.log('불 대수 연산자 OR (||)');
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

// 불 대수 연산자 NOT (!)
console.log('불 대수 연산자 NOT (!)');
console.log(!true);
console.log(!false);
console.log(!!true);
console.log(!!false);